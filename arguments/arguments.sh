#!/bin/bash
echo $0  #this will display file name
echo $1  # this will display the first argument
echo $2   # this will display the second argument on console
echo $@   # this will displays all the arguments on console
echo $*    # this will displays all the arguments on console
echo $#    # this will displays the number of arguments passed on the console
echo $$    # this will displays PID of the current program

